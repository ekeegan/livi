<p>A month after moving to Los Angeles in October of 2011, Olivia was cast in two feature films.  She auditioned for both roles back to back on the same day. <strong><i> Decoding Annie Parker</i></strong>, starring Helen Hunt, Aaron Paul, and Samantha Morton (and many more), is the almost true dramedy about cancer and the discovery of the BRCA1 gene, which has been said to be one of the most significant discoveries of the 20th century.  Olivia plays young Joan in the film and is honored to be part of this important film.  Like too many others, she has been touched by the devastating affects of breast cancer. </p>


